template<typename T, int index>
struct scalar_accessor_4
{
    T _data[4];

    void operator +=(T s) noexcept {
        _data[index] += s;
    }
    void operator -=(T s) noexcept {
        _data[index] -= s;
    }
    void operator *=(T s) noexcept {
        _data[index] *= s;
    }
    void operator /=(T s) noexcept {
        _data[index] /= s;
    }
    void operator  =(T s) noexcept {
        _data[index] = s;
    }
    T& operator++()    noexcept {
        return ++_data[index];
    }
    T  operator++(int) noexcept {
        return _data[index]++;
    }
    T& operator--()    noexcept {
        return --_data[index];
    }
    T  operator--(int) noexcept {
        return _data[index]--;
    }
    operator T() const noexcept {
        return _data[index];
    }
};

template<typename T, int index>
T format_as(const scalar_accessor_4<T, index> &a)
{
    return static_cast<T>(a);
}


template <typename T, int index>
struct scalar_accessor_4i : public scalar_accessor_4<T, index>
{
    void operator %=(T s) noexcept {
        scalar_accessor_4<T,index>::_data[index] %= s;
    }
    void operator |=(T s) noexcept {
        scalar_accessor_4<T,index>::_data[index] |= s;
    }
    void operator &=(T s) noexcept {
        scalar_accessor_4<T,index>::_data[index] &= s;
    }
    void operator ^=(T s) noexcept {
        scalar_accessor_4<T,index>::_data[index] ^= s;
    }
    void operator<<=(T s) noexcept {
        scalar_accessor_4<T,index>::_data[index] <<= s;
    }
    void operator>>=(T s) noexcept {
        scalar_accessor_4<T,index>::_data[index] >>= s;
    }
};

template<typename T, int index>
T format_as(const scalar_accessor_4i<T, index> &a)
{
    return static_cast<T>(a);
}



